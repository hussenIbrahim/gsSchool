String _translateFun(context, String str) {
  if (context != null) {
    return "${AppLocalizations.of(context).translate("$str")}".capitalize();
  }
  return "${AppLocalizations.of(navigatorKey.currentContext).translate("$str")}"
      .capitalize();
}

extension StringExtension on String {
  String capitalize() {
    return "${this[0].toUpperCase()}${this.substring(1)}";
  }

  String trans({BuildContext context}) {
    return _translateFun(context, this);
  }
}
